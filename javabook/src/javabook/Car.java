package javabook;

public class Car {
	String maker;
	String model;
	String color;
	static int curSpeed;
	
	public Car() {
		
	}
	
	public Car(String maker, String model, String color, int curSpeed) {
		this.maker = maker;
		this.model = model;
		this.color = color;
		this.curSpeed = curSpeed;
	}
	
	public int speedUp() {
		return curSpeed += 1;
	}
	
	public int speedDown() {
		return curSpeed -= 1;
	}
	
	public static void main(String[] args) {
		Car car1 = new Car("메이커1", "모델1", "회색", 100);
		Car car2 = new Car("메이커2", "모델2", "검은색", 200);
		Car car3 = new Car("메이커3", "모델3", "흰색", 300);
		
	}
}
